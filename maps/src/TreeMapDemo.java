import java.util.TreeMap;

public class TreeMapDemo {

    public static void main(String[] args) {
        TreeMap<Integer, String> myMap = new TreeMap<>();
        myMap.put(30, "esoft");
        myMap.put(20, "uom");
        myMap.put(40, "iit");
        myMap.put(10, "ijse");
        System.out.println(myMap);
    }
}
